def load_words(filepath='../1900_words_list.md'):
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.readlines()
    words = []
    for line in lines:
        word = line.strip()
        if word and not word.startswith('#'):
            words.append(word)
    return words

def main():
    os.chdir(os.path.dirname(os.path.abspath(__file__)))
    words = load_words()

    assert len(words) == 1900, f"Expected 1900, got {len(words)}"
    assert len(CLASSIFICATIONS) == 1900, f"Classifications: {len(CLASSIFICATIONS)}"

    # 全結果を一つのJSONに
    results = []
    for i, word in enumerate(words):
        word_id = i + 1
        cat = CLASSIFICATIONS[word_id]
        results.append({"id": word_id, "word": word, "categoryId": cat})

    os.makedirs('results', exist_ok=True)
    with open('results/all_results.json', 'w', encoding='utf-8') as f:
        json.dump(results, f, ensure_ascii=False, indent=2)

    print(f"Generated {len(results)} classifications -> results/all_results.json")

if __name__ == '__main__':
    main()
